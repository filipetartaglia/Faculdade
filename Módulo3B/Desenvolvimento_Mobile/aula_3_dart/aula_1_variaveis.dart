// 2. Variáveis e Tipos de Dados
void main() {
  // Variável 'name' que armazena uma string (texto) 
  String name = 'Filipe Tartaglia';

  // Variável 'age' que armazena um número inteiro
  int age = 19;

  // Variável 'height' que armazena um número decimal
  double height = 1.65;

  // Variável 'isStudent' que armazena um valor booleano (verdadeiro ou falso)
  bool isStudent = true;

  print('Name: $name');
  print('Age: $age');
  print('Height: $height');
  print('Is Student: $isStudent');
}